    public void mostrarPantallaCliente(int tipoDeOperacion, Cliente cliente) {
        PantallaCliente pantallaCliente = PantallaCliente.getInstancia(null, true, this.facadadeNegocio);
        this.setVisible(false);
        if(tipoDeOperacion == Constantes.AGREGAR) {
            pantallaCliente.mostrarPantalla(tipoDeOperacion, cliente);
        } else {
            int fila = this.jTable_Cliente.getSelectedRow();
            if (fila != -1) {
                Cliente clienteSeleccion = this.listaDeClientes.get(fila);
                pantallaCliente.mostrarPantalla(tipoDeOperacion, clienteSeleccion);
            } else {
                this.mostrarAdvertencia("Seleccione un cliente", JOptionPane.ERROR_MESSAGE);
            }
        }
        this.cargarClientes();
        this.setVisible(true);
    }
    
