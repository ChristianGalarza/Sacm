    public void cargarClientes() {
        this.listaDeClientes = facadadeNegocio.obtenerClientes();
        String nombreColumnas [] = {"ID","Nombre","Apellido","Edad","Correo","Celular","Cliente Recomendador"};
        String cliente[][]= new String[this.listaDeClientes.size()][7];
        Cliente clienteAuxiliar;
        int saltosdeLinea = 1;
        for (int i = 0; i < this.listaDeClientes.size(); i++) {
            clienteAuxiliar = this.listaDeClientes.get(i);
            cliente[i][0] = Integer.toString(clienteAuxiliar.getIdCliente());
            cliente[i][1] = clienteAuxiliar.getNombre();
            cliente[i][2] = clienteAuxiliar.getApellido();
            cliente[i][3] = Integer.toString(clienteAuxiliar.getEdad());
            cliente[i][4] = clienteAuxiliar.getCorreo();
            cliente[i][5] = clienteAuxiliar.getCelular();
            if ((clienteAuxiliar.getIdClienteRecomendador())!=null) {
                cliente[i][6]= Integer.toString(clienteAuxiliar.getIdClienteRecomendador().getIdCliente());
            }else{
                cliente[i][6]= "";
            }
        }
        jTable_Cliente.setRowHeight(saltosdeLinea*this.ESPACIO_ENTRE_LINEAS);
        
        this.jTable_Cliente.setModel(new DefaultTableModel(cliente, nombreColumnas));
    }
